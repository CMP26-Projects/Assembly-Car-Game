    ROWS:             
                      MOV   FIRSTBYTEINROW, DI
                      MOV   AX, WID
                      MOV   INCOUNTER, AX
    COLS:             
                      CMP   BYTE PTR ES:[DI], 16
                      JE    HANDLECANTDRAW
                      INC   DI
                      DEC   INCOUNTER
                      JNZ   COLS
                      MOV   DI, FIRSTBYTEINROW
                      ADD   DI, SCREENWIDTH
                      DEC   OUTCOUNTER
                      JNZ   ROWS
                      JMP   FINISH

    HANDLECANTDRAW:   
    

                      JMP   FINISHPOWER
    FINISH:           
ENDM






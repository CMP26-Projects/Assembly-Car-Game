    DRAWING_COLLECTED_POWERUP2: 
                                CMP                CarToScan , 0                                                                                                                     ;Car1 is scanning
                                JE                 Car2Powerup2
    ;Powerup for first player is collected

                                MOV                AX , powerup1Posx

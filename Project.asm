;THIS MACRO CHECKS THE WHOLE AREA TO BE DRAWN

CHECKCANDRAW MACRO WID, HEI, STARX, STARY, DIRECTION
                   LOCAL ROWS, COLS, FINISH, HANDLECANTDRAW, CONTINUEUP, CONTINUERIGHT, CONTINUEDOWN, CONTINUELEFT
    ;VIDEO MEMORY
                   MOV   AX, 0A000H
                   MOV   ES, AX
                   MOV   DI, STARY
                   MOV   AX, SCREENWIDTH
                   MUL   DI
                   MOV   DI, AX
                   ADD   DI, STARX
                   MOV   AX, HEI
                   MOV   OUTCOUNTER, AX

    ROWS:          
                   MOV   FIRSTBYTEINROW, DI
                   MOV   AX, WID
                   MOV   INCOUNTER, AX
    COLS:          
                   CMP   BYTE PTR ES:[DI], 20
                   JE    HANDLECANTDRAW
                   CMP   BYTE PTR ES:[DI], 31
                   JE    HANDLECANTDRAW
                   INC   DI
                   DEC   INCOUNTER
                   JNZ   COLS
                   MOV   DI, FIRSTBYTEINROW
                   ADD   DI, SCREENWIDTH
                   DEC   OUTCOUNTER
                   JNZ   ROWS
                   JMP   FINISH

    HANDLECANTDRAW:
                   CMP   DIRECTION, 0
                   JNE   CONTINUEUP
                   MOV   CANTUP, 1
    CONTINUEUP:    
    
                   CMP   DIRECTION, 1
                   JNE   CONTINUERIGHT
                   MOV   CANTRIGHT, 1
    CONTINUERIGHT: 
    
                   CMP   DIRECTION, 2
                   JNE   CONTINUEDOWN
                   MOV   CANTDOWN, 1
    CONTINUEDOWN:  

                   CMP   DIRECTION, 3
                   JNE   CONTINUELEFT
                   MOV   CANTLEFT, 1
    CONTINUELEFT:  

                   JMP   RANDOMIZEPART
    FINISH:        
ENDM





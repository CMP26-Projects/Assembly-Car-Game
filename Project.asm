                                MOV                DL , player2PosX
                                INT                10H

                                MOV                AH , 09
                                LEA                DX , powerupMessage
                                INT                21H

    ;;;;;;;;;; DRAWING ROAD ;;;;;;;;;;;;


    ;DRAWING PART OF ROAD
    ;DRAW BACKGROUNDIMAGE, SCREENWIDTH, SCREENHEIGHT, 0, 0


    STARTROAD:                  
                                MOV                CANTUP, 0
                                MOV                CANTRIGHT, 0
                                MOV                CANTDOWN, 0
                                MOV                CANTLEFT, 0
                                MOV                POWERUPCOUNTER, 0                                                                       ;ADDED THAT WHEN ADDED START PROGRAM
                                MOV                CURPOWERINDEX, 0
                                MOV                CX, 11
                                MOV                TEMPX, 0
                                MOV                TEMPY, 0
    OUTERLOOP:                  
                                PUSH               CX
                                MOV                CX, 20
    INNERLOOP:                  
                                MOV                TMP4, 0
                                DRAW               BACKGROUNDIMAGEPART , BACKGROUNDIMAGEPARTW, BACKGROUNDIMAGEPARTH, TEMPX, TEMPY, TMP4
                                ADD                TEMPX, BACKGROUNDIMAGEPARTW
                                LOOP               INNERLOOP
                                MOV                TEMPX, 0
                                ADD                TEMPY, BACKGROUNDIMAGEPARTH
                                POP                CX
                                LOOP               OUTERLOOP
    ;CALL DRAWBCKGROUND

                                MOV                TMP4, 0
                                MOV                CX, 0
                                DRAW               HORROADIMG , HORROADIMGW, HORROADIMGH, STARTROADX, STARTROADY, TMP4
                                CALL               POINTSAFTERRIGHT
                                DRAW               STARTFLAGIMG, STARTFLAGIMGW, STARTFLAGIMGH, STARTROADX, STARTROADY, TMP4


    ;THIS IS TO RANDOMIZE NUMBER FROM 0 TO 3 TO SPECIFY THE DIRECTON
                                MOV                CX, NUMBEROFPARTS
    RANDOMIZEPART:              
                                CHECKPOSSIBILITIES
    START:                      
                                PUSH               CX
                                CALL               GETSYSTEMTIME
                                POP                CX
                                AND                DL, 3
                                CMP                DL, 0                                                                                   ;UP
                                JE                 CHECKUP
                                CMP                DL, 1                                                                                   ;RIGHT
                                JE                 CHECKRIGHT
                                CMP                DL, 2                                                                                   ;DOWN
                                JE                 CHECKDOWN
                                JMP                CHECKLEFT                                                                               ;left

    CHECKUP:                    
                                CMP                UPDIR, 0
                                JNE                CONTUP
                                MOV                CANTUP, 1
                                JMP                RANDOMIZEPART
    CONTUP:                     

                                MOV                AX, UPDIR
                                MOV                DX, 0
                                MOV                BX, SCREENWIDTH
                                DIV                BX
                                MOV                TEMPX, DX
                                MOV                TEMPY, AX
                                CMP                TEMPY, YNOUP
                                JB                 NOTHANDLEUP
                                JMP                HANDLEUP
    NOTHANDLEUP:                
                                MOV                CANTUP, 1
                                JMP                RANDOMIZEPART


    CHECKRIGHT:                 
                                CMP                RIGHTDIR, 0
                                JNE                CONTRIGHT
                                MOV                CANTRIGHT, 1
                                JMP                RANDOMIZEPART
    CONTRIGHT:                  

                                MOV                AX, RIGHTDIR
                                MOV                DX, 0
                                MOV                BX, SCREENWIDTH
                                DIV                BX
                                MOV                TEMPX, DX
                                MOV                TEMPY, AX
                                CMP                TEMPX, XNORIGHT
                                JA                 NOTHANDLERIGHT
                                JMP                HANDLERIGHT
    NOTHANDLERIGHT:             
                                MOV                CANTRIGHT, 1
                                JMP                RANDOMIZEPART
    
    CHECKDOWN:                  
                                CMP                DOWNDIR, 0
                                JNE                CONTDOWN
                                MOV                CANTDOWN, 1
                                JMP                RANDOMIZEPART
    CONTDOWN:                   

                                MOV                AX, DOWNDIR
                                MOV                DX, 0
                                MOV                BX, SCREENWIDTH
                                DIV                BX
                                MOV                TEMPX, DX
                                MOV                TEMPY, AX
                                CMP                TEMPY, YNODOWN
                                JA                 NOTHANDLEDOWN
                                JMP                HANDLEDOWN
    NOTHANDLEDOWN:              
                                MOV                CANTDOWN, 1
                                JMP                RANDOMIZEPART
    
    CHECKLEFT:                  
                                CMP                LEFTDIR, 0
                                JNE                CONTLEFT
                                MOV                CANTLEFT, 1
                                JMP                RANDOMIZEPART
    CONTLEFT:                   

                                MOV                AX, LEFTDIR
                                MOV                DX, 0
                                MOV                BX, SCREENWIDTH
                                DIV                BX
                                MOV                TEMPX, DX
                                MOV                TEMPY, AX
                                CMP                TEMPX, XNOLEFT
                                JB                 NOTHANDLELEFT
                                JMP                HANDLELEFT
    NOTHANDLELEFT:              
                                MOV                CANTLEFT, 1
                                JMP                RANDOMIZEPART





    ;THIS PART OF HANDLES WAS REVISED
    ;WE CHECK AGAIN HERE FOR THE SCREEN EDGES
    HANDLEUP:                   
                                MOV                TMP, 0
    ;THIS IS TO LEAVE SOME SPACE FOR A ORTHOGONAL PART TO BE DRAWN
                                MOV                TMP1, VERROADIMGH + HORROADIMGH + 2
                                SUB                TEMPY, HORROADIMGH + 2
                                CHECKCANDRAW       VERROADIMGW, TMP1, TEMPX, TEMPY, TMP
                                ADD                TEMPY, HORROADIMGH + 2
                                MOV                TMP4, 1
                                DRAW               VERROADIMG, VERROADIMGW, VERROADIMGH, TEMPX, TEMPY, TMP4
                                CALL               POINTSAFTERUP
                                MOV                LASTDIR, 0

    ;OBSTACLE RANDOMIZATION
                                PUSH               CX
                                PUSH               TEMPX
                                PUSH               TEMPY
                                CALL               GETSYSTEMTIME
                                AND                DL, VERROADIMGW - OBSTACLEW
                                MOV                DH, 0
                                SUB                TEMPX, OBSTACLEW
                                SUB                TEMPX, DX
                                CALL               GETSYSTEMTIME
                                AND                DL, VERROADIMGH - OBSTACLEH - THRESHOLD                                                 ; THIS THRESHOLD TO START FROM 10 TO 40 TO NOT MAKE TWO OBSTACLES IN THE CORNER TOGETHER
                                MOV                DH, 0
                                ADD                TEMPY, THRESHOLD / 2                                                                    ;AS THRESHOLD IS 20 TO START FROM 10
                                ADD                TEMPY, DX
                                MOV                TMP4, 0
                                CALL               OBSTRANDANDDRAW
    
    ;POWERUPRANDOMIZATION
                                POP                TEMPY
                                POP                TEMPX
                                CALL               GETSYSTEMTIME
                                MOV                AL, VERROADIMGW - POWERW
                                MOV                RANGEOFRAND, AL
                                CALL               RANGINGRAND
                                SUB                TEMPX, POWERW
                                SUB                TEMPX, DX
                                CALL               GETSYSTEMTIME
                                MOV                AL, VERROADIMGH - POWERH - THRESHOLD
                                MOV                RANGEOFRAND, AL
                                CALL               RANGINGRAND
                                ADD                TEMPY, THRESHOLD / 2                                                                    ;AS THRESHOLD IS 20 TO START FROM 10
                                ADD                TEMPY, DX
                                MOV                TMP4, 0

    ;WHICH POWERUP
                                CALL               WHICHPOWERIMG
    
                                POP                CX
                                JMP                FINISH



    


    ;THIS THE ONLY DIRECTION WE DONT CALL XY IN IT SO THE DI IS STILL ON TOP LEFT AFTER FINISH DRAWING
    HANDLERIGHT:                
                                MOV                TMP, 1
                                MOV                TMP1, HORROADIMGW + VERROADIMGW + 2
                                CHECKCANDRAW       TMP1, HORROADIMGH, TEMPX, TEMPY, TMP
                                MOV                TMP4, 1
                                DRAW               HORROADIMG, HORROADIMGW, HORROADIMGH, TEMPX, TEMPY, TMP4
                                CALL               POINTSAFTERRIGHT
                                MOV                LASTDIR, 1

                                CMP                CX, 0                                                                                   ;HANDLING FIRST SEGMENT NO OBSTACLES
                                JNE                NOTFIRSTSEGMENT
                                JMP                FIRSTSEGMENT
    NOTFIRSTSEGMENT:            

    ;OBSTACLE RANDOMIZATION
                                PUSH               CX
                                PUSH               TEMPX
                                PUSH               TEMPY
                                CALL               GETSYSTEMTIME
                                AND                DL, HORROADIMGW - OBSTACLEW - THRESHOLD
                                MOV                DH, 0
                                ADD                TEMPX, THRESHOLD / 2
                                ADD                TEMPX, DX
                                CALL               GETSYSTEMTIME
                                AND                DL, HORROADIMGH - OBSTACLEH
                                MOV                DH, 0
                                ADD                TEMPY, DX
                                MOV                TMP4, 0
                                CALL               OBSTRANDANDDRAW


    ;POWERUPRANDOMIZATION
                                POP                TEMPY
                                POP                TEMPX
                                CALL               GETSYSTEMTIME
                                MOV                AL, HORROADIMGW - POWERW - THRESHOLD
                                MOV                RANGEOFRAND, AL
                                CALL               RANGINGRAND
                                ADD                TEMPX, THRESHOLD / 2
                                ADD                TEMPX, DX
                                CALL               GETSYSTEMTIME
                                MOV                AL, HORROADIMGH - POWERH
                                MOV                RANGEOFRAND, AL
                                CALL               RANGINGRAND
                                ADD                TEMPY, DX
                                MOV                TMP4, 0

    ;WHICH POWERUP
                                CALL               WHICHPOWERIMG


                                POP                CX

    FIRSTSEGMENT:               
                                JMP                FINISH
    





    HANDLEDOWN:                 
                                MOV                TMP, 2
                                MOV                TMP1, VERROADIMGH + HORROADIMGH + 2
                                CHECKCANDRAW       VERROADIMGW, TMP1, TEMPX, TEMPY, TMP
                                MOV                TMP4, 1
                                DRAW               VERROADIMG, VERROADIMGW, VERROADIMGH, TEMPX, TEMPY, TMP4
                                MOV                LASTDIR, 2
                                CALL               POINTSAFTERDOWN

    ;OBSTACLE RANDOMIZATION
                                PUSH               CX
                                PUSH               TEMPX
                                PUSH               TEMPY
                                CALL               GETSYSTEMTIME
                                AND                DL, VERROADIMGW - OBSTACLEW
                                MOV                DH, 0
                                SUB                TEMPX, OBSTACLEW
                                SUB                TEMPX, DX
                                CALL               GETSYSTEMTIME
                                AND                DL, VERROADIMGH - OBSTACLEH - THRESHOLD
                                MOV                DH, 0
                                ADD                TEMPY, THRESHOLD / 2
                                ADD                TEMPY, DX
                                MOV                TMP4, 0
                                CALL               OBSTRANDANDDRAW

    ;POWERUPRANDOMIZATION
                                POP                TEMPY
                                POP                TEMPX
                                CALL               GETSYSTEMTIME
                                MOV                AL, VERROADIMGW - POWERW
                                MOV                RANGEOFRAND, AL
                                CALL               RANGINGRAND
                                SUB                TEMPX, POWERW
                                SUB                TEMPX, DX
                                CALL               GETSYSTEMTIME
                                MOV                AL, VERROADIMGH - POWERH - THRESHOLD
                                MOV                RANGEOFRAND, AL
                                CALL               RANGINGRAND
                                ADD                TEMPY, THRESHOLD / 2
                                ADD                TEMPY, DX
                                MOV                TMP4, 0

    ;WHICH POWERUP
                                CALL               WHICHPOWERIMG


                                POP                CX
                                JMP                FINISH



    
    HANDLELEFT:                 
                                MOV                TMP, 3
                                MOV                TMP1, HORROADIMGW + VERROADIMGW + 2
                                SUB                TEMPX, VERROADIMGW + 2
                                CHECKCANDRAW       TMP1, HORROADIMGH, TEMPX, TEMPY, TMP
                                ADD                TEMPX, VERROADIMGW + 2
                                MOV                TMP4, 1
                                DRAW               HORROADIMG, HORROADIMGW, HORROADIMGH, TEMPX, TEMPY, TMP4
                                MOV                LASTDIR, 3
                                CALL               POINTSAFTERLEFT

    ;OBSTACLE RANDOMIZATION
                                PUSH               CX
                                PUSH               TEMPX
                                PUSH               TEMPY
                                CALL               GETSYSTEMTIME
                                AND                DL, HORROADIMGW - OBSTACLEW - THRESHOLD
                                MOV                DH, 0
                                SUB                TEMPX, OBSTACLEW  + THRESHOLD / 2
                                SUB                TEMPX, DX
                                CALL               GETSYSTEMTIME
                                AND                DL, HORROADIMGH - OBSTACLEH
                                MOV                DH, 0
                                ADD                TEMPY, DX
                                MOV                TMP4, 0
                                CALL               OBSTRANDANDDRAW


    ;POWERUPRANDOMIZATION
                                POP                TEMPY
                                POP                TEMPX
                                CALL               GETSYSTEMTIME
                                MOV                AL, HORROADIMGW - POWERW - THRESHOLD
                                MOV                RANGEOFRAND, AL
                                CALL               RANGINGRAND
                                SUB                TEMPX, POWERW  + THRESHOLD / 2
                                SUB                TEMPX, DX
                                CALL               GETSYSTEMTIME
                                MOV                AL, HORROADIMGH - POWERH
                                MOV                RANGEOFRAND, AL
                                CALL               RANGINGRAND
                                ADD                TEMPY, DX
                                MOV                TMP4, 0

    ;WHICH POWERUP
                                CALL               WHICHPOWERIMG


                                POP                CX

    FINISH:                     
    ;INITIALIZING CANT DRAW ARRAY
                                MOV                CANTUP, 0
                                MOV                CANTRIGHT, 0
                                MOV                CANTDOWN, 0
                                MOV                CANTLEFT, 0

                                DEC                CX
                                JNZ                GOUP
                                JMP                LAST
    GOUP:                       
                                JMP                FAR PTR RANDOMIZEPART


    LAST:                       
                                CMP                CX, NUMBEROFPARTS - MINNUMOFPARTS
                                JBE                NOTSTARTPROGRAM
                                CALL               GETSYSTEMTIME2
                                JMP                STARTROAD
    NOTSTARTPROGRAM:            
                                CALL               DRAWENDLINE

    ; MOV CURPOWERINDEX, 4
    ; CALL RETRIEVEROAD

    ; set initial pos of first car in the game
                                MOV                PosXfirst , STARTROADX
                                MOV                PosYfirst , STARTROADY + 1
                                Draw_Car           CarImg1, CAR_SIZE, PosXfirst , PosYfirst, 1

    ; set initial pos of second car in the game
                                MOV                PosXsecond , STARTROADX
                                MOV                PosYsecond , STARTROADY + HORROADIMGH - Car_Size - 1
                                Draw_Car           CarImg2, CAR_SIZE, PosXsecond , PosYsecond, 2

                                MOV                DX , PosXfirst
                                MOV                PosX, DX

                                MOV                DX , PosYfirst
                                MOV                PosY, DX



        

    ; MOV TMP4 , 0
    ; MOV TEMPX , 71
    ; MOV TEMPY , 187
    ; DRAW INCSPEEDPOWER, POWERW, POWERH, TEMPX, TEMPY, TMP4

    ; MOV powerupParent , 1
    ; ClearPower
 

    mainLoop:                   
                                MOV                AX, POWERUPCOUNTER
                                CMP                INDEXSTARTSHOWING, AX
                                JAE                DONTSHOWPOWER

                                MOV                AH, 2CH                                                                                 ; INTERRUPT to get system time
                                INT                21H

                                CMP                DH, CURSECOND
                                JE                 DONTSHOWPOWER
                                MOV                CURSECOND, DH

                                MOV                AL, DH
                                MOV                AH, 0
                                MOV                BL, DURATIONTOSHOWPOWER
                                DIV                BL
                                CMP                AH, 0
                                JNE                DONTSHOWPOWER
                                MOV                SI, OFFSET ISVISIBLEPOWER
                                ADD                SI, INDEXSTARTSHOWING
                                INC                INDEXSTARTSHOWING
                                CMP                BYTE PTR DS:[SI], 0
                                JNE                DONTSHOWPOWER
                                CALL               SHOWHIDDENPOWER


    DONTSHOWPOWER:              

                                MOV                DX , PosXfirst
                                MOV                PrevPosXfirst, DX

                                MOV                DX, PosYfirst
                                MOV                PrevPosYfirst, DX

                                MOV                DX , PosXsecond
                                MOV                PrevPosXsecond, DX

                                MOV                DX , PosYsecond
                                MOV                PrevPosYsecond ,DX


    ;--------------    Overriding INT 9H   ---------------
    ;Disable interrrupts
                                CLI
                       
    ;Saving DS it will be the base of the addressing mode inside the interrupt
                                PUSH               DS
                                MOV                AX , CS
                                MOV                DS , AX

    ;changing interrup vector
                                MOV                AX , 2509H
                                LEA                DX , INT09H
                                INT                21H
                
    ;re-enabling interrupts
                                POP                DS
                                STI
            
                
                                CALL               CheckArrowFlags
                                CALL               checkingPositionChange1

                                CALL               CheckWASDFlags
                                CALL               checkingPositionChange2

 
    ;Delay
                                MOV                CX , 0
                                MOV                DX , 64000D
                                MOV                AH , 86H
                                INT                15H

                                JMP                mainLoop                                                                                ; keep looping
    exit:                       
                                HLT

